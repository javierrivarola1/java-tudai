package tudai.proj;

public class Actividad8 {

	public static void main(String[] args) {

				
		System.out.println("El resultado para and es : " + (true && false));
		System.out.println("El resultado para and es : " + (false && true));
		System.out.println("El resultado para and es : " + (true && true));
		System.out.println("El resultado para and es : " + (false && false));
		
		System.out.println("El resultado para or es: " + (true || false));
		System.out.println("El resultado para or es: " + (false || true));
		System.out.println("El resultado para or es: " + (true || true));
		System.out.println("El resultado para or es: " + (false || false));
		
	}

}